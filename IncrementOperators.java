
public class IncrementOperators {

	public static void main(String[] args) {
		  int n = 6;
	     
		   System.out.println(n++); //post-increment  output-> 6
	       System.out.println(++n); //pre-increment output-> 8
	       
	      int h = 10;
	    		  System.out.println(h++); //10
	      int y = 15;
	              System.out.println(++y); // 16
	}

}
